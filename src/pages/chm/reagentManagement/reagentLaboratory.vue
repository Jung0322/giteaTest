<template>
  <b-container fluid>
    <y-search-box :gridOptions="gridOptions" @enter="getList">
      <b-row slot="search">
        <b-col sm="6" md="6" lg="6" xl="6" class="col-xxl-3">
          <y-plant type="search" filter="chm" v-model="searchParam.plantCd" />
        </b-col>
        <b-col sm="6" md="6" lg="6" xl="6" class="col-xxl-3">
          <!-- 실험실명 -->
          <y-text
            :width="9"
            ui="bootstrap"
            label="L0000001824"
            name="labNm"
            v-model="searchParam.labNm"
          ></y-text>
        </b-col>
      </b-row>
    </y-search-box>
    <!-- <b-row class="mt-3">
